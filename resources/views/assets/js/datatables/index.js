function initDataTables(id, options = {}, lang = ptBR) {
    return $(`#${id}`).DataTable({
        responsive: true,
        language: lang(),
        ...options
    });
}

function removeRow(table, trigger) {
    table.row(trigger.parents('tr'))
        .remove()
        .draw();
}
